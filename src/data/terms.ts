export function getTermByName(name: string): Term | undefined {
  return terms.find(term => term.name === name);
}

export function getTermsByCategory(category: Term['category']): Term[] {
  return terms.filter(term => term.category === category);
}

export function searchTerms(keyword: string): Term[] {
  const lowerKeyword = keyword.toLowerCase();
  return terms.filter(term => 
    term.name.includes(keyword) || 
    term.meaning.toLowerCase().includes(lowerKeyword) ||
    term.plainMeaning.toLowerCase().includes(lowerKeyword)
  );
}
